package com;

public class ForEachLoopDemo {
	public static void main(String[] args) {
		int[] a = {1,3,4,5};
		for(int i:a) {
			System.out.println(i);

		}
		
	System.out.println("---------------------");
	double[] d = {1.2,4.5,6.7,3.2};
	for(double x:d) {
		System.out.println(x);
		
	}
	System.out.println("----------------------");
	String[] san= {"sanchitha","raksha","sona"};
	for(String s:san) {
		System.out.println(s);
		
	}
	}

}
